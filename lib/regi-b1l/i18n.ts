export const b1lDictionaries: Record<B1LLocale, B1LDictionary> = { es, en, fr, de, it, pt, nl };

export const b1lLanguageNames: Record<B1LLocale, string> = {
  es: "Español", en: "English", fr: "Français", de: "Deutsch", it: "Italiano", pt: "Português", nl: "Nederlands",
};

export function isB1LLocale(value: string): value is B1LLocale {
  return (b1lLocales as readonly string[]).includes(value);
}

export function detectB1LLocale(): B1LLocale {
  if (typeof navigator === "undefined") return "es";
  for (const candidate of [navigator.language, ...navigator.languages]) {
    const code = candidate.toLowerCase().split("-")[0];
    if (isB1LLocale(code)) return code;
  }
  return "es";
}
