import type { Place } from "./types";

function city(
  countryCode: string,
  countryName: string,
  slug: string,
  name: string,
  latitude: number,
  longitude: number,
  population: number,
  admin1Name = name,
  aliases: string[] = [],
): Place {
  return {
    id: `priority:${countryCode.toLowerCase()}:${slug}`,
    source: "priority",
    sourceId: slug,
    countryCode,
    countryName,
    admin1Name,
    admin2Name: name,
    municipalityName: name,
    localityName: name,
    latitude,
    longitude,
    population,
    placeType: "city",
    slug,
    aliases,
    isFeatured: true,
    isIndexable: true,
  };
}

